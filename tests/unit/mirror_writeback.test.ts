const FRONTMATTER_CONTENT_FILE = `<!-- Do not edit this file directly. Corrections go through \`neotoma corrections create\`, \`neotoma edit <id>\`, or the Neotoma Inspector. This file is regenerated on every relevant write. -->

---
entity_id: ent_abc123
entity_type: plan
schema_version: 1.0
last_observation_at: 2026-06-01T00:00:00Z
observation_count: 5
computed_at: 2026-06-01T00:00:00Z
title: My Plan
status: active
priority: high
tags:
  - feature
  - q3
---

## Overview

This is the plan body.

It has multiple paragraphs.
`;

const ENTITY_MODE_FILE = `<!-- Do not edit this file directly. Corrections go through \`neotoma corrections create\`, \`neotoma edit <id>\`, or the Neotoma Inspector. This file is regenerated on every relevant write. -->

---
entity_id: ent_abc123
entity_type: plan
schema_version: 1.0
last_observation_at: 2026-06-01T00:00:00Z
observation_count: 5
computed_at: 2026-06-01T00:00:00Z
---

# My Plan

## title

My Plan

## status

active

## provenance

| field | observation_id |
| --- | --- |
| title | obs_1 |
`;

function makeProfile(overrides: Partial<MirrorProfile> = {}): MirrorProfile {
  return {
    id: "test-profile",
    entity_type: "plan",
    filter: {},
    output_path: "/tmp/test-output",
    render_mode: "frontmatter_content",
    content_field: "body",
    allow_disk_writeback: true,
    ...overrides,
  };
}

// ---------------------------------------------------------------------------
// parseMirrorMarkdown
// ---------------------------------------------------------------------------

describe("parseMirrorMarkdown — frontmatter_content mode", () => {
  it("extracts user-defined frontmatter fields, skipping managed system fields", () => {
    const profile = makeProfile({ render_mode: "frontmatter_content", content_field: "body" });
    const fields = parseMirrorMarkdown(FRONTMATTER_CONTENT_FILE, profile);

    // User fields present
    expect(fields.title).toBe("My Plan");
    expect(fields.status).toBe("active");
    expect(fields.priority).toBe("high");
    expect(fields.tags).toEqual(["feature", "q3"]);

    // Managed fields NOT present
    expect(fields).not.toHaveProperty("entity_id");
    expect(fields).not.toHaveProperty("entity_type");
    expect(fields).not.toHaveProperty("schema_version");
    expect(fields).not.toHaveProperty("last_observation_at");
    expect(fields).not.toHaveProperty("observation_count");
    expect(fields).not.toHaveProperty("computed_at");
  });

  it("captures the body after frontmatter as the content_field", () => {
    const profile = makeProfile({ render_mode: "frontmatter_content", content_field: "body" });
    const fields = parseMirrorMarkdown(FRONTMATTER_CONTENT_FILE, profile);

    expect(fields.body).toContain("## Overview");
    expect(fields.body).toContain("This is the plan body.");
  });

  it("strips the DO NOT EDIT header comment before parsing", () => {
    const profile = makeProfile({ render_mode: "frontmatter_content" });
    const fields = parseMirrorMarkdown(FRONTMATTER_CONTENT_FILE, profile);
    // If the DO NOT EDIT comment were not stripped, the frontmatter would not be found.
    expect(fields.title).toBe("My Plan");
  });

  it("returns empty body when no content after frontmatter", () => {
    const fileWithoutBody = `---
entity_id: ent_abc123
entity_type: plan
schema_version: 1.0
title: Empty
---
`;
    const profile = makeProfile({ render_mode: "frontmatter_content", content_field: "body" });
    const fields = parseMirrorMarkdown(fileWithoutBody, profile);
    expect(fields.title).toBe("Empty");
    expect(fields.body).toBeUndefined();
  });
});

describe("parseMirrorMarkdown — entity mode", () => {
  it("extracts ## section fields, skipping provenance", () => {
    const profile = makeProfile({ render_mode: "entity" });
    const fields = parseMirrorMarkdown(ENTITY_MODE_FILE, profile);

    expect(fields.title).toBe("My Plan");
    expect(fields.status).toBe("active");

    // Managed frontmatter fields not present
    expect(fields).not.toHaveProperty("entity_id");
    expect(fields).not.toHaveProperty("schema_version");

    // Provenance section should be skipped
    expect(fields).not.toHaveProperty("provenance");
  });
});

describe("parseMirrorMarkdown — content_only mode", () => {
  it("returns the whole file as the content field", () => {
    const content = "# Hello\n\nThis is the content.";
    const profile = makeProfile({ render_mode: "content_only", content_field: "notes" });
    const fields = parseMirrorMarkdown(content, profile);
    expect(fields.notes).toBe(content.trimEnd());
  });
});

