/**
 * Unit tests for mirror_writeback.ts — disk-to-entity write-back (issue #1776).
 *
 * Covers:
 *   - parseMirrorMarkdown: extracts editable fields, skips managed regions
 *   - parseYamlFrontmatter: handles scalars, arrays, quoted strings
 *   - parseMarkdownSections: ## section parser for entity mode
 *   - extractEntityIdFromFile: reads entity_id from frontmatter
 *   - runMirrorPush:
 *       - blocked by missing allow_disk_writeback flag
 *       - canonical-unchanged → corrections applied
 *       - both-sides-changed → conflict emitted
 *       - dry-run applies nothing
 *       - no base → applies disk state
 */

import { describe, it, expect, vi, beforeEach, afterEach } from "vitest";
import * as path from "node:path";
import * as fs from "node:fs/promises";
import { mkdtempSync, rmSync } from "node:fs";
import { tmpdir } from "node:os";

import {
  parseMirrorMarkdown,
  parseYamlFrontmatter,
  parseMarkdownSections,
  extractEntityIdFromFile,
  runMirrorPush,
  type WritebackApiClient,
} from "../../src/services/mirror_writeback.ts";
import type { MirrorProfile } from "../../src/services/canonical_mirror.ts";

