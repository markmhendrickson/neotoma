describe("parseMarkdownSections", () => {
  it("extracts ## sections as fields", () => {
    const body = `## title\n\nMy Plan\n\n## status\n\nactive\n`;
    const result = parseMarkdownSections(body);
    expect(result.title).toBe("My Plan");
    expect(result.status).toBe("active");
  });

  it("parses JSON code blocks", () => {
    const body = `## metadata\n\n\`\`\`json\n{"key": "value"}\n\`\`\`\n`;
    const result = parseMarkdownSections(body);
    expect(result.metadata).toEqual({ key: "value" });
  });

  it("parses list items from bullet format", () => {
    const body = `## tags\n\n- feature\n- q3\n`;
    const result = parseMarkdownSections(body);
    expect(result.tags).toEqual(["feature", "q3"]);
  });

  it("returns null for _(empty)_ sentinel", () => {
    const body = `## description\n\n_(empty)_\n`;
    const result = parseMarkdownSections(body);
    expect(result.description).toBeNull();
  });
});

