describe("parseYamlFrontmatter", () => {
  it("parses simple scalar key-value pairs", () => {
    const yaml = `title: My Plan\nstatus: active\npriority: high`;
    const result = parseYamlFrontmatter(yaml);
    expect(result.title).toBe("My Plan");
    expect(result.status).toBe("active");
    expect(result.priority).toBe("high");
  });

  it("parses boolean values", () => {
    const yaml = `active: true\narchived: false`;
    const result = parseYamlFrontmatter(yaml);
    expect(result.active).toBe(true);
    expect(result.archived).toBe(false);
  });

  it("parses numeric values", () => {
    const yaml = `count: 42\nscore: 3.14`;
    const result = parseYamlFrontmatter(yaml);
    expect(result.count).toBe(42);
    expect(result.score).toBe(3.14);
  });

  it("parses simple list values", () => {
    const yaml = `tags:\n  - feature\n  - q3\n  - backend`;
    const result = parseYamlFrontmatter(yaml);
    expect(result.tags).toEqual(["feature", "q3", "backend"]);
  });

  it("handles null values", () => {
    const yaml = `title: null\nauthor: ~`;
    const result = parseYamlFrontmatter(yaml);
    expect(result.title).toBeNull();
    expect(result.author).toBeNull();
  });

  it("skips blank lines and comments", () => {
    const yaml = `# comment\ntitle: Test\n\nstatus: active`;
    const result = parseYamlFrontmatter(yaml);
    expect(result.title).toBe("Test");
    expect(result.status).toBe("active");
  });
});

