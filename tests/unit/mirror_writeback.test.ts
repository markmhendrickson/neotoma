// runMirrorPush — behavioral tests
// ---------------------------------------------------------------------------

describe("runMirrorPush", () => {
  let tmpDir: string;

  beforeEach(() => {
    tmpDir = mkdtempSync(path.join(tmpdir(), "neotoma-mirror-push-"));
  });

  afterEach(() => {
    try {
      rmSync(tmpDir, { recursive: true, force: true });
    } catch {
      /* ignore */
    }
  });

  function makeMockApi(overrides: Partial<WritebackApiClient> = {}): WritebackApiClient {
    const defaultGet = vi.fn().mockResolvedValue({
      data: {
        entity_type: "plan",
        snapshot: { title: "My Plan", status: "active", priority: "high" },
      },
      error: null,
      response: { status: 200 },
    });
    const defaultPost = vi.fn().mockResolvedValue({ data: { success: true }, error: null });
    return {
      GET: (overrides.GET as WritebackApiClient["GET"]) ?? defaultGet,
      POST: (overrides.POST as WritebackApiClient["POST"]) ?? defaultPost,
    };
  }

  it("blocks push when allow_disk_writeback is not set", async () => {
    const profile = makeProfile({ allow_disk_writeback: false, output_path: tmpDir });
    const api = makeMockApi();
    const result = await runMirrorPush(profile, api);

    expect(result.errors.length).toBeGreaterThan(0);
    expect(result.errors[0].message).toContain("allow_disk_writeback");
    expect(result.corrections_applied).toBe(0);
  });

  it("blocks push when allow_disk_writeback is undefined (default false)", async () => {
    const profile = makeProfile({ output_path: tmpDir });
    delete (profile as Partial<MirrorProfile>).allow_disk_writeback;
    const api = makeMockApi();
    const result = await runMirrorPush(profile, api);

    expect(result.errors.length).toBeGreaterThan(0);
    expect(result.errors[0].message).toContain("allow_disk_writeback");
    expect(result.corrections_applied).toBe(0);
  });

  it("applies corrections when canonical is unchanged and disk was edited (no prior base)", async () => {
    const profile = makeProfile({
      allow_disk_writeback: true,
      output_path: tmpDir,
      render_mode: "frontmatter_content",
      content_field: "body",
    });

    // Write a mirror file with an edited title.
    const diskContent = `---
entity_id: ent_test01
entity_type: plan
schema_version: 1.0
title: Edited Title
status: active
---

My body content.
`;
    const filePath = path.join(tmpDir, "my-plan.md");
    await fs.writeFile(filePath, diskContent, "utf8");

    const postMock = vi.fn().mockResolvedValue({ data: { success: true }, error: null });
    const getMock = vi.fn().mockResolvedValue({
      data: {
        entity_type: "plan",
        snapshot: { title: "Original Title", status: "active" },
      },
      error: null,
      response: { status: 200 },
    });
    const api = makeMockApi({ GET: getMock, POST: postMock });

    const result = await runMirrorPush(profile, api);

    expect(result.errors).toHaveLength(0);
    expect(result.conflicts).toHaveLength(0);
    expect(result.corrections_applied).toBeGreaterThan(0);

    // The correction should be for the "title" field.
    const titleCorrection = postMock.mock.calls.find(
      (call) => (call[1] as { body: { field: string } }).body.field === "title"
    );
    expect(titleCorrection).toBeDefined();
    if (titleCorrection) {
      expect((titleCorrection[1] as { body: { value: unknown } }).body.value).toBe("Edited Title");
    }
  });

  it("applies corrections when canonical unchanged since base (3-way: canonical==base)", async () => {
    const profile = makeProfile({
      allow_disk_writeback: true,
      output_path: tmpDir,
      render_mode: "frontmatter_content",
      content_field: "body",
    });

    // Simulate a base that matches canonical — disk has been edited.
    const canonicalSnapshot = { title: "Original Title", status: "active" };
    const diskContent = `---
entity_id: ent_test02
entity_type: plan
schema_version: 1.0
title: Disk Edited Title
status: active
---

Body.
`;
    const filePath = path.join(tmpDir, "my-plan.md");
    await fs.writeFile(filePath, diskContent, "utf8");

    // Write a base map where snapshot == canonical (no prior edit).
    const baseDir = path.join(tmpDir, ".neotoma-mirror-base");
    await fs.mkdir(baseDir, { recursive: true });
    const baseMap = {
      "my-plan.md": {
        content_hash: "aabbcc",  // different from disk (disk changed)
        snapshot: canonicalSnapshot,
      },
    };
    await fs.writeFile(path.join(baseDir, "basemap.json"), JSON.stringify(baseMap), "utf8");

    let callCount = 0;
    const getMock = vi.fn().mockImplementation(() => {
      callCount++;
      return Promise.resolve({
        data: { entity_type: "plan", snapshot: canonicalSnapshot },
        error: null,
        response: { status: 200 },
      });
    });
    const postMock = vi.fn().mockResolvedValue({ data: { success: true }, error: null });
    const api = makeMockApi({ GET: getMock, POST: postMock });

    const result = await runMirrorPush(profile, api);

    expect(result.conflicts).toHaveLength(0);
    expect(result.corrections_applied).toBeGreaterThan(0);
    // Title correction should apply the disk value.
    const titleCall = postMock.mock.calls.find(
      (c) => (c[1] as { body: { field: string } }).body.field === "title"
    );
    expect(titleCall).toBeDefined();
  });

  it("emits a conflict when both disk and canonical changed since base", async () => {
    const profile = makeProfile({
      allow_disk_writeback: true,
      output_path: tmpDir,
      render_mode: "frontmatter_content",
      content_field: "body",
    });

    const diskContent = `---
entity_id: ent_conflict01
entity_type: plan
schema_version: 1.0
title: Disk Changed
status: active
---

Body.
`;
    const filePath = path.join(tmpDir, "conflict.md");
    await fs.writeFile(filePath, diskContent, "utf8");

    // Base has original title, canonical also changed to something else.
    const baseDir = path.join(tmpDir, ".neotoma-mirror-base");
    await fs.mkdir(baseDir, { recursive: true });
    const baseMap = {
      "conflict.md": {
        content_hash: "original-hash",
        snapshot: { title: "Original", status: "active" },
      },
    };
    await fs.writeFile(path.join(baseDir, "basemap.json"), JSON.stringify(baseMap), "utf8");

    const getMock = vi.fn().mockResolvedValue({
      data: {
        entity_type: "plan",
        snapshot: { title: "Canonical Changed", status: "active" },
      },
      error: null,
      response: { status: 200 },
    });
    const postMock = vi.fn().mockResolvedValue({ data: { success: true }, error: null });
    const api = makeMockApi({ GET: getMock, POST: postMock });

    const result = await runMirrorPush(profile, api);

    expect(result.conflicts.length).toBeGreaterThan(0);
    const titleConflict = result.conflicts.find((c) => c.field === "title");
    expect(titleConflict).toBeDefined();
    if (titleConflict) {
      expect(titleConflict.base_value).toBe("Original");
      expect(titleConflict.disk_value).toBe("Disk Changed");
      expect(titleConflict.canonical_value).toBe("Canonical Changed");
    }
    // No corrections should be applied when there are conflicts.
    expect(postMock).not.toHaveBeenCalled();
    expect(result.corrections_applied).toBe(0);
  });

  it("dry-run mode applies nothing and reports corrections_dry_run", async () => {
    const profile = makeProfile({
      allow_disk_writeback: true,
      output_path: tmpDir,
      render_mode: "frontmatter_content",
      content_field: "body",
    });

    const diskContent = `---
entity_id: ent_dryrun01
entity_type: plan
schema_version: 1.0
title: Dry Run Edited
status: active
---

Body.
`;
    const filePath = path.join(tmpDir, "dryrun.md");
    await fs.writeFile(filePath, diskContent, "utf8");

    const postMock = vi.fn().mockResolvedValue({ data: { success: true }, error: null });
    const getMock = vi.fn().mockResolvedValue({
      data: {
        entity_type: "plan",
        snapshot: { title: "Original", status: "active" },
      },
      error: null,
      response: { status: 200 },
    });
    const api = makeMockApi({ GET: getMock, POST: postMock });

    const result = await runMirrorPush(profile, api, { dry_run: true });

    // No actual POST calls.
    expect(postMock).not.toHaveBeenCalled();
    expect(result.corrections_applied).toBe(0);
    expect(result.corrections_dry_run).toBeGreaterThan(0);
    expect(result.conflicts).toHaveLength(0);
  });

  it("no-op when disk matches canonical (nothing changed)", async () => {
    const profile = makeProfile({
      allow_disk_writeback: true,
      output_path: tmpDir,
      render_mode: "frontmatter_content",
      content_field: "body",
    });

    // Disk and canonical are identical.
    const diskContent = `---
entity_id: ent_noop01
entity_type: plan
schema_version: 1.0
title: Same Title
status: active
---

Same body.
`;
    const filePath = path.join(tmpDir, "noop.md");
    await fs.writeFile(filePath, diskContent, "utf8");

    const postMock = vi.fn().mockResolvedValue({ data: { success: true }, error: null });
    const getMock = vi.fn().mockResolvedValue({
      data: {
        entity_type: "plan",
        snapshot: { title: "Same Title", status: "active", body: "Same body." },
      },
      error: null,
      response: { status: 200 },
    });
    const api = makeMockApi({ GET: getMock, POST: postMock });

    const result = await runMirrorPush(profile, api);

    expect(postMock).not.toHaveBeenCalled();
    expect(result.corrections_applied).toBe(0);
    expect(result.conflicts).toHaveLength(0);
    expect(result.errors).toHaveLength(0);
  });
});
